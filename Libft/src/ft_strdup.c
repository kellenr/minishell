#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*d;
	unsigned int	i;

	i = 0;
	d = malloc(ft_strlen(s) + 1);
	if (!d)
		return (NULL);
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
